from math import pi

h = 5.0
b = 2.0
r = 1.5

area_parallelogram = h * b
print( 'The area of the parallelogram is %.3f' % area_parallelogram )

area_square = b**2
print( 'The area of the square is %.3f' % area_square )

area_circle = pi * r**2
print( 'The area of the area_circle is %.3f' % area_circle )

volume_cone = b**2
print( 'The volume of the cone is %.3f' % volume_cone )
